/*
Expressões condicionais (if,else if, else)
    - Estabelecem uma pergunta baseada em comparações entre os dados (constantes ou variáveis) de um programa.
    - A resposta dessa comparação é interpretado pela linguagem como um valor logico booleano (verdadeiro ou falso).
    - O booleano é facilmente convertido a inteiros em que: 0-falso, 1-verdadeiro
*/

/*
Operadores Relacionais
== igual
!= diferente
< menor
<= menor ou igual
> maior
>= maior ou igual
*/

/*
Operadores lógicos
&&
||
!
*/

/*
Switch Case 
    - O switch case é geralmente mais limpo e mais fácil de ler quando você está lidando com muitas condições que são todas baseadas no mesmo valor.
    - É mais eficiente do que if em alguns casos, porque o compilador pode otimizar o código para uma pesquisa de tabela constante, em vez de várias comparações.

switch (expressão)
{
    case constante1:
        //comandos1
        break;
        
    case constante2:
        //comandos2
        break;
        
    ...
    
    default: 
        //comandos
}
*/