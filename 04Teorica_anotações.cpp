
/*
FOR
- Comando de repetição incodicional
- O número de repetições é conhecido e fixado antes de entrar no loop. 

for(inicialização;condição;incremento)
{
//comandos a serem executados enquanto a condição for verdadeira
}

Atenção!! Não se usa ; após o for()
*/

/*
WHILE e DO/WHILE
- Comando de repetição condicional
- O número de repetições depende de uma condição, sendo indeterminado

WHILE 
while(expressão)
{
//comandos a serem executados enquanto a expressão for verdadeira
}


DO/WHILE 
-condição é verificada depois que o bloco de comandos é executado pelo menos uma vez
do
{
//comandos a serem executados enquanto a expressão for verdadeira
}
while(expressão)
*/

/*
Comandos de controle de fluxo
- Break - laço é imediatamente interrompido e programa continua com comandos a seguir
- Continue - parecido com o break, mas interrompe a execução da iteração corrente passando para a próxima iteração do laço (ver exemplo no slide)
- Exit() - interrompe o programa como um todo

*/
